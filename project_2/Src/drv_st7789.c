//num 要显示的汉字对应于字库中的数值
//size 要显示的汉字的大小
//mode 对应模式

void LCD_ShowChinese(uint8_t x,uint8_t y,uint8_t num,uint8_t size,uint8_t mode)
{
    uint8_t temp,t,t1;
    uint8_t y0=y;
    uint8_t csize=(size/8 + ((size%8)?1:0)) * size;  //16/8+1
    for(t=0;t<csize;t++)
    {
        if(size==16)
        {
            temp=tim_font_2424[num][t];
        }
        else
            return;
        for(t1=0;t1<8;t1++)
        {
            if(temp&0x80)
                LCD_DrawPoint(x,y,BLACK);
            else
                LCD_DrawPoint(x,y,BLACK);
            temp<<=1;
            y++;
            if((y-y0)==size)
            {
                y=y0;
                x++;
                break;
            }
        }
    }
}

